/**
 * Mostrando como concatenar String utilizando o '+'
 * Mostrando como realizar operações aritimeticas, como soma e divisão
 */
fun main() {
  val primeiroNome = "Daniel "
  val ultimoNome = "Ohata"
  val nomeCompleto = primeiroNome + ultimoNome
  println(nomeCompleto)

  val n1 = 9
  val n2 = 5
  val media = (n1+n2)/2
  println(media)
}